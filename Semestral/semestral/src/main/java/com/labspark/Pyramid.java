package com.labspark;

public class Pyramid {
    Integer id;
    String piramideInicial [];
    String piramideFinal [];

    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public String[] getPiramideInicial() {
        return piramideInicial;
    }
    public void setPiramideInicial(String[] piramideInicial) {
        this.piramideInicial = piramideInicial;
    }
    public String[] getPiramideFinal() {
        return piramideFinal;
    }
    public void setPiramideFinal(String[] piramideFinal) {
        this.piramideFinal = piramideFinal;
    }

}
